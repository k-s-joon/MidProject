package notice.vo;

import java.sql.Date;

public class NoticeVO {
	private int noticeNo;
	private String noticeTitle;
	private String noticeCon;
	private Date noticeDate;
	private Date noticeUpdate;
	private String noticeNick;
	private String memP;
	private String memId;
	
	public int getNoticeNo() {
		return noticeNo;
	}
	public void setNoticeNo(int noticeNo) {
		this.noticeNo = noticeNo;
	}
	public String getNoticeTitle() {
		return noticeTitle;
	}
	public void setNoticeTitle(String noticeTitle) {
		this.noticeTitle = noticeTitle;
	}
	public String getNoticeCon() {
		return noticeCon;
	}
	public void setNoticeCon(String noticeCon) {
		this.noticeCon = noticeCon;
	}
	public Date getNoticeDate() {
		return noticeDate;
	}
	public void setNoticeDate(Date noticeDate) {
		this.noticeDate = noticeDate;
	}
	public Date getNoticeUpdate() {
		return noticeUpdate;
	}
	public void setNoticeUpdate(Date noticeUpdate) {
		this.noticeUpdate = noticeUpdate;
	}
	public String getNoticeNick() {
		return noticeNick;
	}
	public void setNoticeNick(String noticeNick) {
		this.noticeNick = noticeNick;
	}
	public String getMemP() {
		return memP;
	}
	public void setMemP(String memP) {
		this.memP = memP;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	
}
