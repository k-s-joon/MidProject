package free.vo;

public class FComVO {

	public int fComNo;
	public String fComCon;
	public String fComNick;
	public int fNo;
	public String memId;
	public int getfComNo() {
		return fComNo;
	}
	public void setfComNo(int fComNo) {
		this.fComNo = fComNo;
	}
	public String getfComCon() {
		return fComCon;
	}
	public void setfComCon(String fComCon) {
		this.fComCon = fComCon;
	}
	public String getfComNick() {
		return fComNick;
	}
	public void setfComNick(String fComNick) {
		this.fComNick = fComNick;
	}
	public int getfNo() {
		return fNo;
	}
	public void setfNo(int fNo) {
		this.fNo = fNo;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	@Override
	public String toString() {
		return "FComVO [fComNo=" + fComNo + ", fComCon=" + fComCon + ", fComNick=" + fComNick + ", fNo=" + fNo
				+ ", memId=" + memId + "]";
	}
	
	
	
	
	
	
}
