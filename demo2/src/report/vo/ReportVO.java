package report.vo;

import java.sql.Date;

public class ReportVO {

	
public ReportVO() {
		
	}


private int reportNo;
private String reportTitle;
private String reportCon;
private Date reportDate;
private Date reportUpdate;
private String badMemId;
private String goodMemId;


public int getReportNo() {
	return reportNo;
}
public void setReportNo(int reportNo) {
	this.reportNo = reportNo;
}
public String getReportTitle() {
	return reportTitle;
}
public void setReportTitle(String reportTitle) {
	this.reportTitle = reportTitle;
}
public String getReportCon() {
	return reportCon;
}
public void setReportCon(String reportCon) {
	this.reportCon = reportCon;
}
public Date getReportDate() {
	return reportDate;
}
public void setReportDate(Date reportDate) {
	this.reportDate = reportDate;
}
public Date getReportUpdate() {
	return reportUpdate;
}
public void setReportUpdate(Date reportUpdate) {
	this.reportUpdate = reportUpdate;
}
public String getBadMemId() {
	return badMemId;
}
public void setBadMemId(String badMemId) {
	this.badMemId = badMemId;
}
public String getGoodMemId() {
	return goodMemId;
}
public void setGoodMemId(String goodMemId) {
	this.goodMemId = goodMemId;
}


	
}
